create table if not exists public.football_competitions (
  api_football_league_id integer primary key,
  name text not null,
  country text,
  category text not null default 'club',
  season_start_year integer not null,
  priority integer not null default 100,
  enabled boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.football_teams (
  api_football_team_id bigint primary key,
  name text not null,
  code text,
  country text,
  logo_url text,
  founded integer,
  national boolean,
  raw_payload jsonb not null default '{}'::jsonb,
  updated_at timestamptz not null default now()
);

create table if not exists public.football_fixtures (
  api_football_fixture_id bigint primary key,
  competition_api_id integer references public.football_competitions(api_football_league_id),
  season_start_year integer,
  round text,
  kickoff_at timestamptz not null,
  timezone text,
  status text not null,
  status_short text not null,
  elapsed integer,
  venue_id bigint,
  venue_name text,
  venue_city text,
  home_team_api_id bigint references public.football_teams(api_football_team_id),
  away_team_api_id bigint references public.football_teams(api_football_team_id),
  goals_home integer,
  goals_away integer,
  score_halftime_home integer,
  score_halftime_away integer,
  score_fulltime_home integer,
  score_fulltime_away integer,
  raw_payload jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.football_live_snapshots (
  id bigint generated by default as identity primary key,
  api_football_fixture_id bigint not null references public.football_fixtures(api_football_fixture_id) on delete cascade,
  captured_at timestamptz not null default now(),
  status text not null,
  status_short text not null,
  elapsed integer,
  goals_home integer,
  goals_away integer,
  raw_payload jsonb not null default '{}'::jsonb
);

create table if not exists public.model_predictions (
  api_football_fixture_id bigint primary key references public.football_fixtures(api_football_fixture_id) on delete cascade,
  partido_id text,
  home_win_probability numeric(7, 6) not null check (home_win_probability >= 0 and home_win_probability <= 1),
  draw_probability numeric(7, 6) not null check (draw_probability >= 0 and draw_probability <= 1),
  away_win_probability numeric(7, 6) not null check (away_win_probability >= 0 and away_win_probability <= 1),
  expected_home_goals numeric(6, 3) not null,
  expected_away_goals numeric(6, 3) not null,
  predicted_home_goals integer not null,
  predicted_away_goals integer not null,
  confidence numeric(7, 6) not null check (confidence >= 0 and confidence <= 1),
  model_version text not null,
  metadata jsonb not null default '{}'::jsonb,
  generated_at timestamptz not null default now()
);

alter table if exists public.partidos
  add column if not exists api_football_fixture_id bigint,
  add column if not exists api_football_league_id integer,
  add column if not exists temporada integer,
  add column if not exists ronda text,
  add column if not exists minuto integer,
  add column if not exists payload_api jsonb,
  add column if not exists actualizado_api_en timestamptz;

create unique index if not exists partidos_api_football_fixture_id_key
  on public.partidos(api_football_fixture_id)
  where api_football_fixture_id is not null;

create index if not exists football_fixtures_competition_kickoff_idx
  on public.football_fixtures(competition_api_id, kickoff_at);

create index if not exists football_live_snapshots_fixture_time_idx
  on public.football_live_snapshots(api_football_fixture_id, captured_at desc);

create index if not exists model_predictions_generated_at_idx
  on public.model_predictions(generated_at desc);

