create or replace function public.predigol_handle_new_user_profile()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_nombre text;
begin
  v_nombre := nullif(trim(coalesce(new.raw_user_meta_data->>'nombre', '')), '');

  insert into public.profiles (id, nombre, rol, es_admin)
  values (
    new.id,
    coalesce(v_nombre, split_part(new.email, '@', 1), 'Hincha PrediGol'),
    'usuario',
    false
  )
  on conflict (id) do nothing;

  return new;
end;
$$;

drop trigger if exists predigol_handle_new_user_profile_tg on auth.users;
create trigger predigol_handle_new_user_profile_tg
  after insert on auth.users
  for each row
  execute function public.predigol_handle_new_user_profile();

