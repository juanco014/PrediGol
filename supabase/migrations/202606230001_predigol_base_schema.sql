create or replace function public.obtener_mis_ligas()
returns table (
  id uuid,
  nombre text,
  descripcion text,
  codigo text,
  creador_id uuid,
  participantes bigint,
  es_miembro boolean,
  created_at timestamptz
)
language plpgsql
security definer
set search_path = public
as $$
begin
  if auth.uid() is null and coalesce(auth.role(), '') <> 'service_role' then
    raise exception 'Debes iniciar sesion para consultar tus ligas.';
  end if;

  return query
  select
    l.id,
    l.nombre,
    l.descripcion,
    l.codigo,
    l.creador_id,
    count(lm_total.usuario_id)::bigint as participantes,
    true as es_miembro,
    l.created_at
  from public.ligas l
  join public.liga_miembros lm_self on lm_self.liga_id = l.id
  left join public.liga_miembros lm_total on lm_total.liga_id = l.id
  where coalesce(auth.role(), '') = 'service_role'
    or lm_self.usuario_id = auth.uid()
  group by l.id, l.nombre, l.descripcion, l.codigo, l.creador_id, l.created_at
  order by l.created_at desc;
end;
$$;

create or replace function public.obtener_detalle_liga(p_liga_id uuid)
returns table (
  id uuid,
  nombre text,
  descripcion text,
  codigo text,
  creador_id uuid,
  participantes bigint,
  es_miembro boolean,
  created_at timestamptz,
  miembros jsonb
)
language plpgsql
security definer
set search_path = public
as $$
begin
  if auth.uid() is null and coalesce(auth.role(), '') <> 'service_role' then
    raise exception 'Debes iniciar sesion para consultar la liga.';
  end if;

  if coalesce(auth.role(), '') <> 'service_role'
    and not exists (
      select 1
      from public.liga_miembros lm
      where lm.liga_id = p_liga_id
        and lm.usuario_id = auth.uid()
    ) then
    raise exception 'No haces parte de esta liga.';
  end if;

  return query
  select
    l.id,
    l.nombre,
    l.descripcion,
    l.codigo,
    l.creador_id,
    count(lm.usuario_id)::bigint as participantes,
    true as es_miembro,
    l.created_at,
    coalesce(
      jsonb_agg(
        jsonb_build_object(
          'usuario_id', lm.usuario_id,
          'nombre', coalesce(nullif(trim(p.nombre), ''), 'Hincha PrediGol'),
          'username', p.username,
          'avatar_url', p.avatar_url,
          'rol', lm.rol,
          'created_at', lm.created_at
        )
        order by lm.created_at asc
      ) filter (where lm.usuario_id is not null),
      '[]'::jsonb
    ) as miembros
  from public.ligas l
  left join public.liga_miembros lm on lm.liga_id = l.id
  left join public.profiles p on p.id = lm.usuario_id
  where l.id = p_liga_id
  group by l.id, l.nombre, l.descripcion, l.codigo, l.creador_id, l.created_at;
end;
$$;

