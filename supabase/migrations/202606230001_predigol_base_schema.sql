create extension if not exists pgcrypto with schema extensions;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  nombre text,
  username text,
  avatar_url text,
  es_admin boolean not null default false,
  rol text not null default 'usuario',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint profiles_rol_check check (rol in ('usuario', 'admin'))
);

create table if not exists public.partidos (
  id bigint primary key,
  torneo text not null,
  fecha_texto text,
  fecha_orden timestamptz not null,
  local_nombre text not null,
  visitante_nombre text not null,
  local_corto text,
  visitante_corto text,
  -- Canonical stored value is en_vivo; UI labels may render it as "En vivo".
  estado text not null default 'proximo',
  goles_local_final integer,
  goles_visitante_final integer,
  api_football_fixture_id bigint,
  api_football_league_id integer,
  temporada integer,
  ronda text,
  minuto integer,
  payload_api jsonb not null default '{}'::jsonb,
  actualizado_api_en timestamptz,
  origen_datos text not null default 'manual',
  fuente_detalle text,
  creado_manual_en timestamptz,
  es_relevante boolean not null default false,
  prioridad_visual integer not null default 100,
  external_source text,
  external_id text,
  importado_externo_en timestamptz,
  raw_import_payload jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint partidos_estado_check check (estado in ('proximo', 'en_vivo', 'finalizado', 'cancelado')),
  constraint partidos_goles_local_final_check check (goles_local_final is null or goles_local_final >= 0),
  constraint partidos_goles_visitante_final_check check (goles_visitante_final is null or goles_visitante_final >= 0),
  constraint partidos_prioridad_visual_check check (prioridad_visual >= 0),
  constraint partidos_api_football_fixture_id_key unique (api_football_fixture_id)
);

create table if not exists public.pronosticos (
  id bigint generated by default as identity primary key,
  usuario_id uuid not null references auth.users(id) on delete cascade,
  partido_id bigint not null references public.partidos(id) on delete cascade,
  goles_local integer not null,
  goles_visitante integer not null,
  creado_en timestamptz not null default now(),
  actualizado_en timestamptz not null default now(),
  constraint pronosticos_marcador_local_check check (goles_local >= 0 and goles_local <= 99),
  constraint pronosticos_marcador_visitante_check check (goles_visitante >= 0 and goles_visitante <= 99),
  constraint pronosticos_partido_usuario_key unique (partido_id, usuario_id)
);

create table if not exists public.ligas (
  id uuid primary key default gen_random_uuid(),
  nombre text not null check (char_length(trim(nombre)) >= 3),
  descripcion text,
  codigo text not null,
  creador_id uuid not null references auth.users(id) on delete cascade,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint ligas_codigo_key unique (codigo),
  constraint ligas_codigo_format_check check (codigo = upper(codigo) and char_length(codigo) between 6 and 20)
);

create table if not exists public.liga_miembros (
  id bigint generated by default as identity primary key,
  liga_id uuid not null references public.ligas(id) on delete cascade,
  usuario_id uuid not null references auth.users(id) on delete cascade,
  rol text not null default 'miembro',
  created_at timestamptz not null default now(),
  constraint liga_miembros_rol_check check (rol in ('owner', 'miembro')),
  constraint liga_miembros_liga_usuario_key unique (liga_id, usuario_id)
);

