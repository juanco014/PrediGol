create or replace function public.obtener_api_football_monitor()
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_config jsonb;
  v_runs jsonb;
  v_summary jsonb;
begin
  if not public.predigol_es_admin() then
    raise exception 'No tienes permiso para consultar el monitor de API-Football.';
  end if;

  select jsonb_build_object(
    'id', config.id,
    'enabled', config.enabled,
    'sync_upcoming', config.sync_upcoming,
    'sync_live', config.sync_live,
    'sync_results', config.sync_results,
    'season', config.season,
    'upcoming_limit', config.upcoming_limit,
    'updated_at', config.updated_at,
    'last_note', config.last_note
  )
  into v_config
  from public.predigol_api_football_sync_config config
  where config.id = 'default';

  select coalesce(jsonb_agg(to_jsonb(run) order by run.started_at desc), '[]'::jsonb)
  into v_runs
  from (
    select *
    from public.api_football_sync_runs
    order by started_at desc
    limit 12
  ) run;

  select jsonb_build_object(
    'runs_24h', count(*) filter (where started_at >= now() - interval '24 hours'),
    'requests_24h', coalesce(sum(requests_count) filter (
      where started_at >= now() - interval '24 hours'
    ), 0),
    'errors_24h', count(*) filter (
      where started_at >= now() - interval '24 hours' and status = 'error'
    ),
    'partials_24h', count(*) filter (
      where started_at >= now() - interval '24 hours' and status = 'partial'
    ),
    'last_success_at', max(finished_at) filter (where status = 'success')
  )
  into v_summary
  from public.api_football_sync_runs;

  return jsonb_build_object(
    'config', coalesce(v_config, '{}'::jsonb),
    'runs', coalesce(v_runs, '[]'::jsonb),
    'summary', coalesce(v_summary, '{}'::jsonb)
  );
end;
$$;

revoke all on function public.obtener_api_football_monitor()
  from public, anon, authenticated;
grant execute on function public.obtener_api_football_monitor()
  to authenticated, service_role;

