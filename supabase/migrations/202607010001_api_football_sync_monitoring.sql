create table if not exists public.api_football_sync_runs (
  id bigint generated by default as identity primary key,
  started_at timestamptz not null default now(),
  finished_at timestamptz,
  duration_ms integer,
  trigger_source text not null default 'manual'
    check (trigger_source in ('manual', 'pg_cron', 'unknown')),
  mode text not null
    check (mode in ('live', 'upcoming', 'results', 'range', 'all')),
  season integer,
  date_from date,
  date_to date,
  request_limit integer,
  status text not null default 'running'
    check (status in ('running', 'success', 'partial', 'error')),
  requests_count integer not null default 0,
  fixtures_count integer not null default 0,
  teams_count integer not null default 0,
  partidos_count integer not null default 0,
  live_snapshots_count integer not null default 0,
  skipped_count integer not null default 0,
  daily_limit integer,
  daily_remaining integer,
  minute_limit integer,
  minute_remaining integer,
  error_message text,
  result jsonb not null default '{}'::jsonb
);

